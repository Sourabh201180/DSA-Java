public class Infinite {
    public static void main(String[] args) {
        // infinite for loop
        for( ; ; ) {
            System.out.println("Hello duniya walo");
        }

        // infinite while loop
        while(true) {
            System.out.println("Hello duniya walo");
        }

        // infinite do-while loop
        do {
            System.out.println("Hello duniya walo");
        } while(true);
    }   
}